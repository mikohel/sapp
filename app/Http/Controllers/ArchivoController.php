     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //

        $archivo  = $request -> all();
        $archivo['uuid'] = (string) Str::uuid();

        if($request -> hasFile('dir_archivo')){

            $archivo['dir_archivo'] = time() . '_' . $request -> file('dir_archivo')->getClientOriginalName();
            $request->file('dir_archivo')->storeAs('folder_archivo', $archivo['dir_archivo']);

        }

        $archivo ['descripcion'] = $request['decripcion'];
        Archivo::create($archivo);
        return redirect()->route('archivo.index');


    }

